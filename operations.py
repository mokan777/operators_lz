# Ввод числа 
n = int(input())

# Инициализация счетчика шагов
count = 0

# Цикл продолжается до тех пор, пока n не станет равно 0
while n != 0:
    # Если n делится на 3 без остатка
    if n % 3 == 0:
        # Вычитаем 3 из n
        n -= 3
    # Если n при делении на 3 дает остаток 1
    elif n % 3 == 1:
        # Вычитаем 1 из n
        n -= 1
    # Иначе (остаток 2)
    else:  # n % 3 == 2
        # Вычитаем 2 из n
        n -= 2
    # Увеличиваем счетчик шагов на 1
    count += 1

# Выводим общее количество шагов
print(count)
